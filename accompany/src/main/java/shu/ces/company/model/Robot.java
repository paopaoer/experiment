package shu.ces.company.model;

public class Robot {

    private int robot_id;
    private String robot_name;
    private String robot_setting;

    public int getRobot_id() {
        return robot_id;
    }

    public void setRobot_id(int robot_id) {
        this.robot_id = robot_id;
    }

    public String getRobot_name() {
        return robot_name;
    }

    public void setRobot_name(String robot_name) {
        this.robot_name = robot_name;
    }

    public String getRobot_setting() {
        return robot_setting;
    }

    public void setRobot_setting(String robot_setting) {
        this.robot_setting = robot_setting;
    }
}
